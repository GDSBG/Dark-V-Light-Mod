	@SubscribeEvent
	public void addFeatureToBiomes(BiomeLoadingEvent event) {
		boolean biomeCriteria = false;
		if (new ResourceLocation("dark_vs_light:holo_forest").equals(event.getName()))
			biomeCriteria = true;
		if (new ResourceLocation("dark_vs_light:glow_forest").equals(event.getName()))
			biomeCriteria = true;
		if (new ResourceLocation("dark_vs_light:enchanted_forest").equals(event.getName()))
			biomeCriteria = true;
		if (new ResourceLocation("nether_wastes").equals(event.getName()))
			biomeCriteria = true;
		if (new ResourceLocation("the_end").equals(event.getName()))
			biomeCriteria = true;
		if (!biomeCriteria)
			return;
		event.getSpawns().getSpawner(EntityClassification.MONSTER).add(new MobSpawnInfo.Spawners(entity, 5, 1, 3));
	}

